function resolveValue(locale: Locale, key: string): string {
  const segments = key.split('.')
  const fallbackSegments = key.split('.')
  let current: string | TranslationDictionary | undefined = translations[locale]
  let fallback: string | TranslationDictionary | undefined = translations.en

  for (const segment of segments) {
    current = typeof current === 'object' && current !== null ? current[segment] : undefined
  }

  for (const segment of fallbackSegments) {
    fallback = typeof fallback === 'object' && fallback !== null ? fallback[segment] : undefined
  }

  const value = typeof current === 'string' ? current : typeof fallback === 'string' ? fallback : key
  return value
}

function interpolate(value: string, params: Record<string, string | number>): string {
  return Object.entries(params).reduce((current, [key, replacement]) => {
    return current.replaceAll(`{${key}}`, String(replacement))
  }, value)
}

export function translate(locale: string | null | undefined, key: string, params: Record<string, string | number> = {}): string {
  const normalizedLocale: Locale = locale === 'en' ? 'en' : 'de'
  return interpolate(resolveValue(normalizedLocale, key), params)
}

export function useTranslate() {
  const { locale } = usePage<PageProps>().props

  return (key: string, params: Record<string, string | number> = {}) => translate(locale, key, params)
}
